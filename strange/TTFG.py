def zongge(name):
    """
    获取名字总格数
    :param name: 名字笔画数字典
    :return: 名字总格数
    """
    sum = 0
    for i in range(len(name)):
        sum += name[i]
    return sum

def five_frame_fleeting_method(age):
    """
    五格流年法
    :param age: 年龄
    :return: 看何种运格
    """
    if age <= 12:
        return "天格、地格"
    if age >= 13 and age <= 24:
        return "人格、地格"
    if age >= 25 and age <= 36:
        return "人格、外格"
    if age >= 37 and age <= 48:
        return "天格、人格"
    if age >= 49:
        return "总格"


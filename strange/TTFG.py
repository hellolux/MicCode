def get_cn_strokes(chara):
    """
    获取汉字的笔画数
    :param chara: 汉字
    :return: 笔画数
    """
    sql_query = "select stroke_count from zi_dataset where zi='{}';".format(chara)
    res = pd.read_sql_query(sql_query, engine)
    return int(res['stroke_count'][0][:-1])

def strokes_digits_five_elements(number):
    """
    获取对应笔画数字个位数的五行
    :param number: 数字
    :return: 五行
    """
    if number in (1,2):
        return "木"
    elif number in (3,4):
        return "火"
    elif number in (5,6):
        return "土"
    elif number in (7,8):
        return "金"
    elif number in (9,0):
        return "水"
    else:
        return "不在五行中"


def tiange(name,nameLen):
    """
    获取名字天格数
    :param name: 名字笔画字典
    :param nameLen: 名字长度
    :return: 天格数
    """
    if nameLen in (2,3):
        return name[0] + 1
    elif nameLen == 4:
        return name[0] + name[1]
    else:
        print("只支持2、3、4个字的名字")
        sys.exit()

